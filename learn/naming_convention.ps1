# PowerShell uses a verb-noun pair for the names of cmdlets and for their derived Microsoft .NET 
# Framework classes. For example, the Get-Command cmdlet provided by PowerShell is used to retrieve 
# all the commands that are registered in PowerShell. The verb part of the name identifies the 
# action that the cmdlet performs. The noun part of the name identifies the entity on which the
# action is performed.


